char	*ft_strrchr(const char *s, int c)
{
	char	*p;
	int		i;
	int		f;

	p = (char *)s;
	i = 0;
	f = 0;
	while (s[i] != '\0')
	{
		if (s[i] == c)
			f = i;
		i++;
	}
	while (f > 0)
	{
		p++;
		f--;
	}
	return (p);
}
