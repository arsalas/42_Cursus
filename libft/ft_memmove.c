#include <stddef.h>

void	*ft_memmove(void *dst, const void *src, size_t len)
{
	char	*s;
	char	*d;

	s = (char *)src;
	d = (char *)dst;
	while (len > 0)
	{
		*d = *s;
		s++;
		d++;
		len--;
	}
	return (dst);
}
